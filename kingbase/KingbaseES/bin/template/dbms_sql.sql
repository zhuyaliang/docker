         
CREATE OR REPLACE PACKAGE DBMS_SQL AS
  i INTEGER;
  j INTEGER;
  cursor_row_count INTEGER;
  cursor_is_open bool;
  
  FUNCTION open_cursor() RETURNS INTEGER;
  PROCEDURE parse(c IN INTEGER, qry IN TEXT,IN language_flag INTEGER);
  PROCEDURE close_cursor(c INOUT INTEGER);
  PROCEDURE bind_variable(c IN INTEGER, pos IN INTEGER,val IN ANYELEMENT);
  PROCEDURE define_column(c IN INTEGER,pos IN INTEGER, val IN ANYELEMENT);
  PROCEDURE column_value(c IN INTEGER,pos IN INTEGER,val INOUT ANYELEMENT);
  PROCEDURE variable_value(c IN INTEGER,pos IN INTEGER,val INOUT ANYELEMENT);
  FUNCTION execute(c IN INTEGER) RETURNS INTEGER;
  FUNCTION fetch_rows(c IN INTEGER) RETURNS INTEGER;
  FUNCTION is_open(c IN INTEGER) RETURNS BOOLEAN;
  FUNCTION execute_and_fetch(c IN INTEGER, exact IN BOOLEAN DEFAULT FALSE)RETURNS INTEGER;
  FUNCTION last_row_count() RETURNS INTEGER;
END;
