CREATE OR REPLACE PACKAGE DBMS_ALERT AS
  PROCEDURE REGISTER(IN INNAME VARCHAR(4000));
  PROCEDURE SIGNAL(IN INNAME NAME,IN INMESSAGE VARCHAR(4000));
  PROCEDURE WAITONE(IN INRELNAME NAME,OUT OUTMESSAGE VARCHAR(1800),OUT OUTSTATUS INTEGER,IN TIMEOUT NUMERIC(10,2) DEFAULT 86400000);
  PROCEDURE WAITANY(OUT OUTRELNAME NAME,OUT OUTMESSAGE VARCHAR(1800),OUT OUTSTATUS INTEGER,IN TIMEOUT NUMERIC(10,2) DEFAULT 86400000);
  PROCEDURE REMOVE(IN INNAME NAME);
  PROCEDURE REMOVEALL();
  PROCEDURE SET_DEFAULTS(IN SENSITIVITY NUMERIC(38,0));
END;